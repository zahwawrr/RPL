package com.example.parkuy.Model;

public class Parkir {
    private String namaTempatParkir;
    private String alamatTempatParkir;
    private String hargaTempatParkir;
    private String incrementTempatParkir;

    public String getNamaTempatParkir() {
        return namaTempatParkir;
    }

    public void setNamaTempatParkir(String namaTempatParkir) {
        this.namaTempatParkir = namaTempatParkir;
    }

    public String getAlamatTempatParkir() {
        return alamatTempatParkir;
    }

    public void setAlamatTempatParkir(String alamatTempatParkir) {
        this.alamatTempatParkir = alamatTempatParkir;
    }

    public String getHargaTempatParkir() {
        return hargaTempatParkir;
    }

    public void setHargaTempatParkir(String hargaTempatParkir) {
        this.hargaTempatParkir = hargaTempatParkir;
    }

    public String getIncrementTempatParkir() {
        return incrementTempatParkir;
    }

    public void setIncrementTempatParkir(String incrementTempatParkir) {
        this.incrementTempatParkir = incrementTempatParkir;
    }
}
